package AnimalRescue;

public class AnimalRescuer {
    private String name;
    private int age;
    private int money;

    public String getNume(){
        return name;
    }

    public void setNume(String name){
        this.name = name;
    }


    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getMoney(){
        return money;
    }

    public void setMoney(int money){
        this.money = money;
    }

}
